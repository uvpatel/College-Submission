#include <stdio.h>

// Program 5: Arithmetic operators demonstration
int main() {
    // Initialize variables as per problem requirement
    int X = 25;
    int Y = 4;
    
    // Display initial values
    printf("X = %d, Y = %d\n\n", X, Y);
    
    // Perform and display arithmetic operations
    printf("Arithmetic Operations:\n");
    printf("Addition (X + Y): %d\n", X + Y);
    printf("Subtraction (X - Y): %d\n", X - Y);
    printf("Multiplication (X * Y): %d\n", X * Y);
    printf("Division (X / Y): %d\n", X / Y);
    printf("Modulo (X %% Y): %d\n", X % Y);
    
    return 0;
}
