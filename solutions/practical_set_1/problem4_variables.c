#include <stdio.h>

// Program 4: Variable declaration and initialization
int main() {
    // Declaring and initializing variables of different types
    int integerVar = 42;
    float floatVar = 3.14f;
    char charVar = 'A';
    double doubleVar = 123.456;
    
    // Printing values of all variables
    printf("Integer value: %d\n", integerVar);
    printf("Float value: %f\n", floatVar);
    printf("Character value: %c\n", charVar);
    printf("Double value: %lf\n", doubleVar);
    
    return 0;
}
